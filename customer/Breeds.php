<b>Housing:</b><br>
For keeping our cows healthy, productive and diseases free, good housing is very important. Providing the space inside and outside depending on the breeds. Usually for each cow we are maintaining 80 square feet open space and 40 square feet space inside the house. Although it may vary depending on the size of the breed. We have proper ventilation system, and ensure sufficient flow of fresh air and light inside the house and the house is made up of Concrete.<br>
<b>Feeding:</b><br>
Good foods ensure good health and maximum production. So we always give proper diet to our cows adequate amount of high quality and nutritious foods to ensure proper growth and good health. Forages, which refer especially to hay or straw, are the most common type of feed used for raising dairy cows. For fulfilling energy demands, add cereal grains to the dairy cow’s feed. Greens help to maximize milk production. So add greens as much as possible to their regular foods. Greens also help to reduce feeding costs. Alfalfa, timothy, clover etc. Are considered as good greens for dairy cows. If possible, make a grazing place or pasture for your cows. Along with adequate nutritious foods, ensure availability of adequate amount of clean and fresh water. Dairy cows require more water than any other animals. Because they are mainly raised for their milk production, and their milk contain a large amount of water. Usually a dairy cow about 5 liters of water to producing milk. We always provide them sufficient amount of water according to their demand.<br>

<b>Care & Management:</b><br>
Taking good care and proper management is the main step to be successful in any animal husbandry business. So always try to take good care of your cows. Feed them nutritious food and provide sufficient amount of clean and fresh water. Vaccinate them timely, So that you can keep them free from all types of diseases or illness. If possible, always stock some necessary drugs. Keep their house clean and wash the cow regularly.<br>

<b>Milking:</b><br>
